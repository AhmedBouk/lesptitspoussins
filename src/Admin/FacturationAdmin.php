<?php


namespace App\Admin;


use App\Entity\Facturation;
use Sonata\AdminBundle\Admin\AbstractAdmin;
use Sonata\AdminBundle\Datagrid\DatagridMapper;
use Sonata\AdminBundle\Datagrid\ListMapper;
use Sonata\AdminBundle\Form\FormMapper;
use Sonata\AdminBundle\Show\ShowMapper;

class FacturationAdmin extends AbstractAdmin
{

    public function toString($object)
    {
        return $object instanceof Facturation
            ? $object->getId()
            : 'ID';
    }

    protected function configureFormFields(FormMapper $form)
    {
        parent::configureFormFields($form); // TODO: Change the autogenerated stub
        $form
            ->add('date')
            ->add('montant_ht', null, [
                'label' => 'Son Montant Hors-Taxe'
            ])
            ->add('tva')
            ->add('total')
            ;
    }

    protected function configureListFields(ListMapper $list)
    {
        parent::configureListFields($list); // TODO: Change the autogenerated stub
        $list
            ->addIdentifier('id', null, [
                'route' => [
                    'name' => 'show'
                ]
            ])
            ->add('date')
            ->add('montant_ht', null, [
                'label' => 'Son Montant Hors-Taxe'
            ])            ->add('tva')
            ->add('total')
            ->add('parentt', null, ['associated_property' => 'nom', 'label' => 'Parent'])
            ->add('proProfil', null, ['associated_property' => 'nom_entreprise', 'label' => 'Entrprise'])
            ->add('actions', 'actions', [
                'actions' => [
                    'edit' => [],
                    'delete' => []
                ]
            ])
        ;
    }

    protected function configureDatagridFilters(DatagridMapper $filter)
    {
        parent::configureDatagridFilters($filter); // TODO: Change the autogenerated stub
        $filter
            ->add('id')
            ->add('date')
            ;
    }

    protected function configureShowFields(ShowMapper $show)
    {
        parent::configureShowFields($show); // TODO: Change the autogenerated stub
        $show
            ->add('date')
            ->add('montant_ht', null, [
                'label' => 'Son Montant Hors-Taxe'
            ])            ->add('tva')
            ->add('total')
            ->add('parentt', null, ['associated_property' => 'nom', 'label' => 'Parent'])
            ->add('proProfil', null, ['associated_property' => 'nom_entreprise', 'label' => 'Entrprise'])
            ->add('created_at', null, [
                'label' => 'Date de CrÃ©ation'
            ])
            ->add('updated_at', null, [
                'label' => 'Date de Modification'
            ])
            ;
    }

}